def precision(rater_a, rater_b):
	correct = len([i for i in range(len(rater_a)) if rater_a[i] == rater_b[i]])
	#print("Correct: %d, Overall: %d" % (correct, len(rater_a)))
	return correct / float(len(rater_a))


